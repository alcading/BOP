package com.huateng.report.scheduler.job.domain;

public class UserAuthorityXMLDomain {
	private String gelid;
    private String username;
    private String privilegecontainername;
    private String privilegename;
    private String privilegevalue;
	public String getGelid() {
		return gelid;
	}
	public void setGelid(String gelid) {
		this.gelid = gelid;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPrivilegecontainername() {
		return privilegecontainername;
	}
	public void setPrivilegecontainername(String privilegecontainername) {
		this.privilegecontainername = privilegecontainername;
	}
	public String getPrivilegename() {
		return privilegename;
	}
	public void setPrivilegename(String privilegename) {
		this.privilegename = privilegename;
	}
	public String getPrivilegevalue() {
		return privilegevalue;
	}
	public void setPrivilegevalue(String privilegevalue) {
		this.privilegevalue = privilegevalue;
	}
    
    
}
